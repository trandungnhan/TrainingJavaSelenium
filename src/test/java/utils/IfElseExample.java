package utils;

public class IfElseExample {
    public static void main(String[] args){
        //defining a variable
        int number=14;
        //Check if the number is divisible by 2 or not
        if(number%2==0){
            System.out.println(number+ " is even number");
        }else{
            System.out.println(number+ " is odd number");
        }
    }
}
